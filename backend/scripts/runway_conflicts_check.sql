SELECT
  ws.id                        AS schedule_id,
  al.name                      AS airline,
  ac.registration              AS aircraft_reg,
  at.full_name                 AS aircraft_type,
  at.min_runway_takeoff_m      AS need_takeoff_m,
  at.min_runway_landing_m      AS need_landing_m,
  ws.departure_airport         AS dep,
  ws.arrival_airport           AS arr,
  s_dep.new_m                  AS dep_new_runway,
  s_arr.new_m                  AS arr_new_runway,
  CASE
    WHEN s_dep.new_m IS NOT NULL AND at.min_runway_takeoff_m > s_dep.new_m
         AND s_arr.new_m IS NOT NULL AND at.min_runway_landing_m > s_arr.new_m
      THEN 'BOTH'
    WHEN s_dep.new_m IS NOT NULL AND at.min_runway_takeoff_m > s_dep.new_m
      THEN 'TAKEOFF_DEP'
    WHEN s_arr.new_m IS NOT NULL AND at.min_runway_landing_m > s_arr.new_m
      THEN 'LANDING_ARR'
  END                           AS problem
FROM weekly_schedule ws
JOIN aircraft        ac ON ac.id = ws.aircraft_id
JOIN aircraft_types  at ON at.id = ac.aircraft_type_id
JOIN airlines        al ON al.id = ac.airline_id
LEFT JOIN shortenings s_dep ON s_dep.iata = ws.departure_airport
LEFT JOIN shortenings s_arr ON s_arr.iata = ws.arrival_airport
WHERE (s_dep.new_m IS NOT NULL AND at.min_runway_takeoff_m > s_dep.new_m)
   OR (s_arr.new_m IS NOT NULL AND at.min_runway_landing_m > s_arr.new_m)
ORDER BY airline, aircraft_reg, dep, arr;
